package com.example.qr;



class member {
    private String name;
    private String sur;
    private long en;
    private String p;

    public member() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSur() {
        return sur;
    }

    public void setSur(String sur) {
        this.sur = sur;
    }

    public long getEn() {
        return en;
    }

    public void setEn(long en) {
        this.en = en;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }
}
